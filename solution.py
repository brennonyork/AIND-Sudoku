def naked_twins(values):
    """Eliminate values using the naked twins strategy.
    Args:
        values(dict): a dictionary of the form {'box_name': '123456789', ...}

    Returns:
        the values dictionary with the naked twins eliminated from peers.
    """
    # Find all instances of naked twins
    for unit in unitlist: # for each unit we want to find like pairs
        naked_twins = {}
        for cell in unit:
            # sort the list to ensure proper string comparison (eg '23' == '23')
            nums = list(values[cell])
            nums.sort()
            nums = ''.join(nums)

            # check for naked twins possibility
            if len(nums) == 2:
                naked_twins[nums] = naked_twins.get(nums, list()) + [cell]
        # find all naked twin pairs from the set of possibilities
        nt_pairs = [[nums, cells] for nums, cells in naked_twins.items() if len(cells) == 2]
        # remove all nt_pairs found
        for nt_vals, nt_cells in nt_pairs:
            # iterate over cells within unit that are not the naked twins cells
            for cell in [u for u in unit if u not in nt_cells]:
                values[cell] = ''.join([v for v in values[cell] if v not in list(nt_vals)])
    return values


